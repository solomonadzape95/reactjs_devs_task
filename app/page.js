let cur
export default function Page() {
  const [lvl, setLvl] = useState(0)
  const title = useRef(Menu);
 cur = title.current
  function handleClick(nav){
title.current = nav.children
setLvl(l => l+1)
  }
function handleBack(nav){
title.current = nav.parent
setLvl(l => l-1)
}
  return (
    (<div className="flex flex-col bg-background">
      <header
        className="flex items-center justify-start gap-8 bg-black text-white px-4 py-3 text-primary-foreground">
          {lvl > 0 && <button onClick={()=> {handleBack(cur)}}>
          <ArrowLeftIcon className="h-5 w-5" />
          <span className="sr-only">Back</span></button>}
        <h1 className="text-xl font-bold">{title.current.name}</h1>
      </header>
      <nav className="flex flex-col border-red-500">
        {
          title.current.map(nav =>
            <Link href={nav.url || '#'} onClick={() => {
              nav.children && handleClick(nav)
            }} className="flex items-center justify-between gap-3 hover:bg-gray-100 p-2" key={nav.title}>

            <span className="text-lg font-medium flex items-center gap-2">{nav.icon}{nav.title}</span>
            {nav.children && <ChevronRightIcon/>}
          </Link>
          )
        }

      </nav>
    </div>)
  );
}

